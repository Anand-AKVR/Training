package com.model;

public class Rectangle {
	
	
	private int leanth;

	private int breadth;

	public Rectangle() {
		super();
	}

	public Rectangle(int leanth, int breadth) {
		super();
		this.leanth = leanth;
		this.breadth = breadth;
	}

	public int getLeanth() {
		return leanth;
	}

	public void setLeanth(int leanth) {
		this.leanth = leanth;
	}

	public int getBreadth() {
		return breadth;
	}

	public void setBreadth(int breadth) {
		this.breadth = breadth;
	}
	

}

	